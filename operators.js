//in javascript, operators are used to perform operations on variables  and values

//1. Arthimetic operator

let a = 5;
let b = 3;
console.log(a + b)// addition
console.log(a - b)//subtraction
console.log(a * b)//multiplication
console.log(a / b)//division
console.log(a % b)//modulus
console.log(a ** b)//a power b
console.log(a++)//increment by +1
console.log(a--)//decrement by -1

//2. assignment operators
let c = 10;
let d = 5;

//add and assign 
c += d; // c=c + d -> c = 15
console.log(c); //15

//subtract and assign
c -= d; //c = c - d -> c = 10

// multiply and assign
c *= d; // c = c - d -> c = 50

//divide and assign
c /= d; // c = c / d -> = 2

//modulus and assign 
c %= d; // c = c % d -> = 0
console.log(c);

// exponentiation and assign 
c = 2;
c **= d; //c = c ** d -> 2(5) = 32
console.log(c); //32


